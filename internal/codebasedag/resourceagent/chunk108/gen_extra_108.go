const (
	// DefaultMaxPids_108 is the default maximum number of pids allowed.
	DefaultMaxPids_108 int64 = 65536

	// DefaultWarnPercent_108 is the default warning threshold percentage.
	DefaultWarnPercent_108 = 80

	// MinWarnPercent_108 is the minimum allowed warning percentage.
	MinWarnPercent_108 = 1

	// MaxWarnPercent_108 is the maximum allowed warning percentage.
	MaxWarnPercent_108 = 100

	// HardMaxPids_108 is the absolute maximum pids value (kernel limit).
	HardMaxPids_108 int64 = 4194304

	// MinMaxPids_108 is the minimum valid pids max value.
	MinMaxPids_108 int64 = 1

	// MaxPidsUnlimited_108 is the string representation for unlimited.
	MaxPidsUnlimited_108 = "max"

	// WarningNone_108 indicates no warning.
	WarningNone_108 WarningLevel_108 = iota

	// WarningLow_108 indicates low warning severity.
	WarningLow_108

	// WarningMedium_108 indicates medium warning severity.
	WarningMedium_108

	// WarningHigh_108 indicates high warning severity.
	WarningHigh_108

	// WarningCritical_108 indicates critical warning severity.
	WarningCritical_108
)

// ============================================================
// Error variables
// ============================================================

var (
	ErrInvalidMaxPids_108       = errors.New("invalid pids.max value: must be positive integer or 'max'")
	ErrNegativeLimit_108        = errors.New("pids.max cannot be negative")
	ErrExceedsHardLimit_108     = fmt.Errorf("pids.max exceeds hard limit of %d", HardMaxPids_108)
	ErrInvalidWarnPercent_108   = fmt.Errorf("warning percentage must be between %d and %d", MinWarnPercent_108, MaxWarnPercent_108)
	ErrPolicyNil_108            = errors.New("policy pointer is nil")
	ErrUsageNil_108             = errors.New("usage pointer is nil")
	ErrCurrentExceedsMax_108    = errors.New("current pids usage exceeds max")
	ErrNegativeCurrent_108      = errors.New("current pids usage cannot be negative")
	ErrDuplicateEntry_108       = errors.New("duplicate threshold entry")
	ErrThresholdOutOfOrder_108  = errors.New("threshold entries must be in increasing order of min percent")
)

