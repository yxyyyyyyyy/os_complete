// GetRecommendedAction_108 returns the recommended action for a given warning level.
func GetRecommendedAction_108(level WarningLevel_108) string {
	for _, entry := range DefaultThresholdTable_108 {
		if entry.Level == level {
			return entry.Action
		}
	}
	return "Unknown level"
}

// GetThresholdForPercent_108 returns the threshold entry for a given percent.
func GetThresholdForPercent_108(percent float64) *ThresholdEntry_108 {
	for _, entry := range DefaultThresholdTable_108 {
		if percent >= float64(entry.MinPercent) && percent < float64(entry.MaxPercent) {
			return &entry
		}
	}
	return nil
}

// SortThresholdTable_108 sorts the table by MinPercent ascending.
func SortThresholdTable_108(table []ThresholdEntry_108) {
	sort.Slice(table, func(i, j int) bool {
		return table[i].MinPercent < table[j].MinPercent
	})
}

// GenerateWarningMessage_108 creates a formatted warning message.
func GenerateWarningMessage_108(level WarningLevel_108, current, max int64, policyName string) string {
	percent := ComputeUsagePercent_108(current, max)
	return fmt.Sprintf("[%s] %s: PIDs usage at %s (current=%d, max=%d, policy=%s)",
		level.String_108(), GetRecommendedAction_108(level), FormatUsagePercent_108(percent), current, max, policyName)
}

// DetermineWarningLevelFromPolicy_108 returns the warning level based on the policy threshold.
func DetermineWarningLevelFromPolicy_108(policy *PidsMaxPolicy_108, usage *PidsUsage_108) WarningLevel_108 {
	if !policy.EnableWarnings {
		return WarningNone_108
	}
	percent := ComputeUsagePercent_108(usage.Current, usage.Max)
	if percent >= float64(policy.WarnPercent) {
		// Use threshold table to get severity
		return GetWarningLevel_108(percent, DefaultThresholdTable_108)
	}
	return WarningNone_108
}

// ============================================================
// Policy Validation Suite
// ============================================================

// ValidatePolicyConsistency_108 performs multiple checks and returns a list of errors.
func ValidatePolicyConsistency_108(policy *PidsMaxPolicy_108) []error {
	var errs []error
	if policy == nil {
		return append(errs, ErrPolicyNil_108)
	}
	if err := ValidatePidsMax_108(policy.Max); err != nil {
		errs = append(errs, err)
	}
	if policy.WarnPercent < MinWarnPercent_108 || policy.WarnPercent > MaxWarnPercent_108 {
		errs = append(errs, ErrInvalidWarnPercent_108)
	}
	computedThreshold := calculateWarnThreshold_108(policy.Max, policy.WarnPercent)
	if policy.WarnThreshold != computedThreshold {
		errs = append(errs, fmt.Errorf("warn threshold mismatch: stored %d, computed %d", policy.WarnThreshold, computedThreshold))
	}
	if policy.PolicyName == "" {
		errs = append(errs, errors.New("policy name should not be empty"))
	}
	return errs
}

// ValidatePolicyAgainstHardLimit_108 checks that policy values are within system limits.
func ValidatePolicyAgainstHardLimit_108(policy *PidsMaxPolicy_108) error {
	if policy.Max > HardMaxPids_108 {
		return ErrExceedsHardLimit_108
	}
	return nil
}

// ============================================================
// Metrics and Statistics
// ============================================================

// CalculateWarningStatistics_108 computes simple statistics over a slice of usage.
func CalculateWarningStatistics_108(usages []*PidsUsage_108) (avgPercent float64, maxPercent float64, warningCount int) {
	if len(usages) == 0 {
		return 0, 0, 0
	}
	totalPercent := 0.0
	maxPercent = 0.0
	warningCount = 0
	for _, u := range usages {
		p := ComputeUsagePercent_108(u.Current, u.Max)
		totalPercent += p
		if p > maxPercent {
			maxPercent = p
		}
		if p >= float64(DefaultWarnPercent_108) {
			warningCount++
		}
	}
	avgPercent = totalPercent / float64(len(usages))
	return avgPercent, maxPercent, warningCount
}

