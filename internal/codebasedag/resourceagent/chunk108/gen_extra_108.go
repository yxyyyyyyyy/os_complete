var DefaultThresholdTable_108 = []ThresholdEntry_108{
	{Level: WarningNone_108, MinPercent: 0, MaxPercent: 50, Action: "No action needed"},
	{Level: WarningLow_108, MinPercent: 50, MaxPercent: 70, Action: "Monitor pids usage; consider scaling"},
	{Level: WarningMedium_108, MinPercent: 70, MaxPercent: 85, Action: "Increase pids.max or reduce processes"},
	{Level: WarningHigh_108, MinPercent: 85, MaxPercent: 95, Action: "Immediate action required: check runaway processes"},
	{Level: WarningCritical_108, MinPercent: 95, MaxPercent: 100, Action: "Critical: pids exhaustion imminent"},
}

