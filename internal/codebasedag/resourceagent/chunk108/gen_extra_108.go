// NewPidsMaxPolicy_108 creates a new policy with the given max and warn percent.
// Returns an error if parameters are invalid.
func NewPidsMaxPolicy_108(max int64, warnPercent int, name string) (*PidsMaxPolicy_108, error) {
	if err := ValidatePidsMax_108(max); err != nil {
		return nil, err
	}
	if warnPercent < MinWarnPercent_108 || warnPercent > MaxWarnPercent_108 {
		return nil, ErrInvalidWarnPercent_108
	}
	policy := &PidsMaxPolicy_108{
		Max:            max,
		WarnPercent:    warnPercent,
		WarnThreshold:  calculateWarnThreshold_108(max, warnPercent),
		PolicyName:     name,
		EnableWarnings: true,
	}
	return policy, nil
}

// DefaultPolicy_108 creates a policy with default values.
func DefaultPolicy_108() *PidsMaxPolicy_108 {
	p, _ := NewPidsMaxPolicy_108(DefaultMaxPids_108, DefaultWarnPercent_108, "default")
	return p
}

// NewPidsUsage_108 creates a new usage snapshot.
func NewPidsUsage_108(current, max int64, timestamp int64) *PidsUsage_108 {
	return &PidsUsage_108{
		Current:   current,
		Max:       max,
		Timestamp: timestamp,
	}
}

// ============================================================
// Validation Functions
// ============================================================

// ValidatePidsMax_108 checks that the limit is valid.
func ValidatePidsMax_108(limit int64) error {
	if limit < 0 {
		return ErrNegativeLimit_108
	}
	if limit == 0 {
		return ErrInvalidMaxPids_108
	}
	if limit > HardMaxPids_108 {
		return ErrExceedsHardLimit_108
	}
	return nil
}

// ValidatePolicy_108 performs full validation of a policy.
func ValidatePolicy_108(policy *PidsMaxPolicy_108) error {
	if policy == nil {
		return ErrPolicyNil_108
	}
	if err := ValidatePidsMax_108(policy.Max); err != nil {
		return err
	}
	if policy.WarnPercent < MinWarnPercent_108 || policy.WarnPercent > MaxWarnPercent_108 {
		return ErrInvalidWarnPercent_108
	}
	return nil
}

// ValidateUsage_108 checks that usage is valid.
func ValidateUsage_108(usage *PidsUsage_108) error {
	if usage == nil {
		return ErrUsageNil_108
	}
	if usage.Current < 0 {
		return ErrNegativeCurrent_108
	}
	if usage.Max > 0 && usage.Current > usage.Max {
		return ErrCurrentExceedsMax_108
	}
	return nil
}

// ValidateThresholdTable_108 validates a threshold table.
func ValidateThresholdTable_108(table []ThresholdEntry_108) error {
	if len(table) == 0 {
		return errors.New("threshold table cannot be empty")
	}
	seen := make(map[WarningLevel_108]bool)
	prevMax := 0
	for i, entry := range table {
		if entry.MinPercent < 0 || entry.MaxPercent > 100 || entry.MinPercent >= entry.MaxPercent {
			return fmt.Errorf("entry %d: invalid percent range [%d, %d)", i, entry.MinPercent, entry.MaxPercent)
		}
		if entry.MinPercent != prevMax {
			return ErrThresholdOutOfOrder_108
		}
		if seen[entry.Level] {
			return ErrDuplicateEntry_108
		}
		seen[entry.Level] = true
		prevMax = entry.MaxPercent
	}
	if prevMax != 100 {
		return errors.New("threshold table must cover 0-100%")
	}
	return nil
}

// ============================================================
// Parsing / Formatting
// ============================================================

// ParsePidsMax_108 parses a string into a max value.
// Accepts "max" (unlimited) or a positive integer.
func ParsePidsMax_108(s string) (int64, error) {
	s = strings.TrimSpace(s)
	if strings.EqualFold(s, MaxPidsUnlimited_108) {
		return math.MaxInt64, nil
	}
	val, err := strconv.ParseInt(s, 10, 64)
	if err != nil {
		return 0, fmt.Errorf("%w: %s", ErrInvalidMaxPids_108, s)
	}
	if val < 0 {
		return 0, ErrNegativeLimit_108
	}
	return val, nil
}

// FormatPidsMax_108 formats a max value as a string.
// Uses "max" for math.MaxInt64, otherwise decimal.
func FormatPidsMax_108(max int64) string {
	if max == math.MaxInt64 {
		return MaxPidsUnlimited_108
	}
	return strconv.FormatInt(max, 10)
}

// FormatUsagePercent_108 formats a usage percentage to one decimal place.
func FormatUsagePercent_108(percent float64) string {
	return fmt.Sprintf("%.1f%%", percent)
}

// ============================================================
// Computation Helpers
// ============================================================

// calculateWarnThreshold_108 computes the absolute threshold from max and warnPercent.
func calculateWarnThreshold_108(max int64, warnPercent int) int64 {
	if max <= 0 {
		return 0
	}
	threshold := int64(math.Ceil(float64(max) * float64(warnPercent) / 100.0))
	if threshold > max {
		threshold = max
	}
	return threshold
}

// ComputeUsagePercent_108 returns the usage percentage as a float64 (0-100).
func ComputeUsagePercent_108(current, max int64) float64 {
	if max <= 0 {
		return 0.0
	}
	if current >= max {
		return 100.0
	}
	return float64(current) * 100.0 / float64(max)
}

// IsNearLimit_108 checks if usage is at or above the warning threshold.
func IsNearLimit_108(policy *PidsMaxPolicy_108, usage *PidsUsage_108) bool {
	if policy == nil || usage == nil {
		return false
	}
	if !policy.EnableWarnings {
		return false
	}
	if policy.WarnThreshold <= 0 {
		return false
	}
	return usage.Current >= policy.WarnThreshold
}

// ============================================================
// Warning Level Determination
// ============================================================

// GetWarningLevel_108 returns the warning level for a given usage percentage.
func GetWarningLevel_108(percent float64, table []ThresholdEntry_108) WarningLevel_108 {
	if len(table) == 0 {
		table = DefaultThresholdTable_108
	}
	for _, entry := range table {
		if percent >= float64(entry.MinPercent) && percent < float64(entry.MaxPercent) {
			return entry.Level
		}
	}
	// Exactly 100% -> last entry (critical)
	if percent >= 100.0 {
		return WarningCritical_108
	}
	return WarningNone_108
}

// GetWarningLevelFromUsage_108 is a convenience function.
func GetWarningLevelFromUsage_108(usage *PidsUsage_108) WarningLevel_108 {
	percent := ComputeUsagePercent_108(usage.Current, usage.Max)
	return GetWarningLevel_108(percent, DefaultThresholdTable_108)
}

// ============================================================
// Warning and Policy Application
// ============================================================

// CheckWarning_108 evaluates usage against policy and returns warning level and message.
func CheckWarning_108(policy *PidsMaxPolicy_108, usage *PidsUsage_108) (WarningLevel_108, string) {
	if err := ValidatePolicy_108(policy); err != nil {
		return WarningNone_108, fmt.Sprintf("invalid policy: %v", err)
	}
	if err := ValidateUsage_108(usage); err != nil {
		return WarningNone_108, fmt.Sprintf("invalid usage: %v", err)
	}
	if !policy.EnableWarnings {
		return WarningNone_108, "warnings disabled"
	}
	percent := ComputeUsagePercent_108(usage.Current, usage.Max)
	level := GetWarningLevel_108(percent, DefaultThresholdTable_108)
	msg := fmt.Sprintf("PIDs usage at %s (current=%d, max=%d, policy=%s)",
		FormatUsagePercent_108(percent), usage.Current, usage.Max, policy.PolicyName)
	return level, msg
}

// ApplyPolicy_108 applies the policy to the usage and returns a warning event if warranted.
func ApplyPolicy_108(policy *PidsMaxPolicy_108, usage *PidsUsage_108) *WarningEvent_108 {
	level, msg := CheckWarning_108(policy, usage)
	if level == WarningNone_108 {
		return nil
	}
	return &WarningEvent_108{
		Level:      level,
		Message:    msg,
		Timestamp:  usage.Timestamp,
		PolicyName: policy.PolicyName,
	}
}

// ============================================================
// Policy Mutators
// ============================================================

// SetMax_108 updates the max value and recalculates threshold.
func SetMax_108(policy *PidsMaxPolicy_108, newMax int64) error {
	if err := ValidatePidsMax_108(newMax); err != nil {
		return err
	}
	policy.Max = newMax
	policy.WarnThreshold = calculateWarnThreshold_108(policy.Max, policy.WarnPercent)
	return nil
}

// SetWarnPercent_108 updates the warning percent and recalculates threshold.
func SetWarnPercent_108(policy *PidsMaxPolicy_108, newPercent int) error {
	if newPercent < MinWarnPercent_108 || newPercent > MaxWarnPercent_108 {
		return ErrInvalidWarnPercent_108
	}
	policy.WarnPercent = newPercent
	policy.WarnThreshold = calculateWarnThreshold_108(policy.Max, policy.WarnPercent)
	return nil
}

// UpdateUsage_108 updates the usage record with a new current value.
func UpdateUsage_108(usage *PidsUsage_108, current int64) error {
	if usage == nil {
		return ErrUsageNil_108
	}
	if current < 0 {
		return ErrNegativeCurrent_108
	}
	usage.Current = current
	if usage.Max <= 0 {
		usage.Max = DefaultMaxPids_108
	}
	return nil
}

// ============================================================
// Policy Comparison and Merging
// ============================================================

// MergePolicies_108 merges two policies, taking the more restrictive of each field.
func MergePolicies_108(p1, p2 *PidsMaxPolicy_108) *PidsMaxPolicy_108 {
	if p1 == nil && p2 == nil {
		return DefaultPolicy_108()
	}
	if p1 == nil {
		return p2
	}
	if p2 == nil {
		return p1
	}
	merged := &PidsMaxPolicy_108{}
	if p1.Max < p2.Max {
		merged.Max = p1.Max
	} else {
		merged.Max = p2.Max
	}
	if p1.WarnPercent < p2.WarnPercent {
		merged.WarnPercent = p1.WarnPercent
	} else {
		merged.WarnPercent = p2.WarnPercent
	}
	merged.WarnThreshold = calculateWarnThreshold_108(merged.Max, merged.WarnPercent)
	merged.PolicyName = fmt.Sprintf("merged(%s,%s)", p1.PolicyName, p2.PolicyName)
	merged.EnableWarnings = p1.EnableWarnings && p2.EnableWarnings
	return merged
}

// ComparePolicies_108 compares two policies for equality.
func ComparePolicies_108(a, b *PidsMaxPolicy_108) bool {
	if a == nil || b == nil {
		return a == b
	}
	return a.Max == b.Max &&
		a.WarnPercent == b.WarnPercent &&
		a.PolicyName == b.PolicyName &&
		a.EnableWarnings == b.EnableWarnings
}

