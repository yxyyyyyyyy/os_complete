// WarningLevel_108 defines the severity level of a pids warning.
type WarningLevel_108 int

// String returns the string representation of the warning level.
func (w WarningLevel_108) String_108() string {
	switch w {
	case WarningNone_108:
		return "none"
	case WarningLow_108:
		return "low"
	case WarningMedium_108:
		return "medium"
	case WarningHigh_108:
		return "high"
	case WarningCritical_108:
		return "critical"
	default:
		return fmt.Sprintf("unknown(%d)", int(w))
	}
}

// PidsMaxPolicy_108 defines the policy for near-limit warnings.
type PidsMaxPolicy_108 struct {
	// Max is the maximum number of pids allowed.
	Max int64

	// WarnPercent is the usage percentage at which warnings begin.
	WarnPercent int

	// WarnThreshold is the absolute value at which warnings trigger (computed).
	WarnThreshold int64

	// PolicyName is an optional human-readable name for the policy.
	PolicyName string

	// EnableWarnings enables or disables warning generation.
	EnableWarnings bool
}

// PidsUsage_108 holds the current and maximum pids usage data.
type PidsUsage_108 struct {
	// Current is the current number of pids used.
	Current int64

	// Max is the maximum number of pids allowed (same as policy.Max).
	Max int64

	// Timestamp is an optional Unix timestamp of the usage snapshot.
	Timestamp int64
}

// ThresholdEntry_108 defines a single threshold in a table.
type ThresholdEntry_108 struct {
	// Level is the warning level for this threshold.
	Level WarningLevel_108

	// MinPercent is the lower bound percentage (inclusive).
	MinPercent int

	// MaxPercent is the upper bound percentage (exclusive, unless 100).
	MaxPercent int

	// Action is a recommended action string.
	Action string
}

// WarningEvent_108 represents a warning event generated by the policy.
type WarningEvent_108 struct {
	// Level is the severity level.
	Level WarningLevel_108

	// Message is a human-readable warning message.
	Message string

	// Timestamp is the Unix timestamp when the event was generated.
	Timestamp int64

	// PolicyName is the name of the policy that generated the event.
	PolicyName string
}

